package lesson001;

public class ExampleSwitch {

    void exampleSwitch() {
        int a = 50;
        switch (a) {
            case 50:
                System.out.println("a equal 50");
                break;
            case 100:
                System.out.println("a equal 100");
                break;
            default:
                System.out.println("a isn't equal 50 and 100");

        }


    }
}



